#include <cstdio>
#include <cmath>
#define _USE_MATH_DEFINES


int main() {
    float x1 = 1e-4;
    printf("%.2f\n", 1);
    float x2 = 24.33e5;
    printf("%.2f\n", x2);
    float x3 = M_PI;
    printf("%.2f\n", x3);
    float x4 = M_E;
    printf("%.2f\n", x4);
    double x5 = sqrt(5);
    printf("%.2lf\n", x5);
    long double x6 = log(100);
    printf("%.2Lf\n", x6);

    return 0;
}
